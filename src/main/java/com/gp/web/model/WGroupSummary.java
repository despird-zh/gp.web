package com.gp.web.model;

public class WGroupSummary {
	
	private int sumDocs ;
	
	private int sumSubGroups;
	
	private int sumTopics;
	
	private int sumMembers;
	
	private int sumExtMbrs;

	public int getSumDocs() {
		return sumDocs;
	}

	public void setSumDocs(int sumDocs) {
		this.sumDocs = sumDocs;
	}

	public int getSumSubGroups() {
		return sumSubGroups;
	}

	public void setSumSubGroups(int sumSubGroups) {
		this.sumSubGroups = sumSubGroups;
	}

	public int getSumTopics() {
		return sumTopics;
	}

	public void setSumTopics(int sumTopics) {
		this.sumTopics = sumTopics;
	}

	public int getSumMembers() {
		return sumMembers;
	}

	public void setSumMembers(int sumMembers) {
		this.sumMembers = sumMembers;
	}

	public int getSumExtMbrs() {
		return sumExtMbrs;
	}

	public void setSumExtMbrs(int sumExtMbrs) {
		this.sumExtMbrs = sumExtMbrs;
	}

	
}
